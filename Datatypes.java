public class Datatypes {

    public static void main(String[] args) {

        int a = 100;
        System.out.println("Line-1: " + a);

        a = 0144;
        System.out.println("Line-2: " + a);

        a = 0x64;
        System.out.println("Line-3: " + a);

        byte b = 127;
        System.out.println("Line-4: " + b);

        char c = 'A';
        System.out.println("Line-5: " + c);

        String d = "ABC(123";
        System.out.println("Line-6: " + d);

        double e = 23.4;
        System.out.println("Line-7: " + e);

        float f = 23.0f;
        System.out.println("Line-8: " + f);

        long g = 2345234234234L;
        System.out.println("Line-9: " + g);

        short h = 32767;
        System.out.println("Line-10: " + h);

        boolean v = true;
        System.out.println("Line-11: " + v);

    }

}
